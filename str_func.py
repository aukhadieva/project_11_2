def str_func(string):
    """
    Функция, которая принимает на вход строку и возвращает ее со всеми заглавными буквами.
    """
    return string.upper()


def str_func2(string):
    """
    Функция, которая делает заглавными первые буквы каждого слова в строке, поступившей на вход функции.
    """
    return string.title()

def str_func3(string):
    """
    Функция, которая делает строчными все буквы каждого слова в строке, поступившей на вход функции.
    """
    return string.lower()
